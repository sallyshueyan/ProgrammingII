import random
print(random.randint(5, 20))  # line 1
# smallest number: 5 largest number: 20
print(random.randrange(3, 10, 2))  # line 2
# smallest number: 3 largest number: 9,  4 is not possible
print(random.uniform(2.5, 5.5))  # line 3
# smallest number: 2.5 largest number: 5.5

print(random.randint(1,100)) # produce a random number between 1 and 100 inclusive.

